#include<stdio.h>
int main(){
    // integer type Variale.
    int age=10;
    printf("My age is %d\n", age);

    // floating type Variale.
    float gpa=4.56;
    printf("My Result is %f\n", gpa);

    // Double type Variale.
    double result=2.92;
    printf("My Result is %.2lf\n", result);

    // Character type Variale.
    char initial='A';
    printf("My Result is %c\n", initial);

    // String type Variale.
     char myName[]="Masud";
    printf("My Result is %s\n", myName);

    return 0;
}